package ningbo.media.data.entity;


public class LocationData implements EntityData{
	
	private Integer id ;
	
	private String name ;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
