package com.jshop.bean.taobao;

public class TbItem {

	private int num_iid;
	private String title;
	private String nick;
	public int getNum_iid() {
		return num_iid;
	}
	public void setNum_iid(int numIid) {
		num_iid = numIid;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	
}
